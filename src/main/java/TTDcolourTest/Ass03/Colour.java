package TTDcolourTest.Ass03;

public class Colour {
    private float Red;
    private float Green;
    private float Blue;

    public Colour(float Red, float Green, float Blue) {
        this.Red = Red;
        this.Green = Green;
        this.Blue = Blue;
    }
    public float getRed() {
        return Red;
    }

    public float getGreen() {
        return Green;
    }

    public float getBlue() {
        return Blue;
    }
}
